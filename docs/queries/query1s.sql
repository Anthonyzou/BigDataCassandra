FROM smallcdr 
WHERE 
(MSC_CODE ,CITY_ID,SERVICE_NODE_ID
,RUM_DATA_NUM ,DUP_SEQ_NUM ,SEIZ_CELL_NUM ,FLOW_DATA_INC 
,SUB_HOME_INT_PRI ,CON_OHM_NUM,SESS_SFC) 
> (10000,10000,10000,3,10000,1,10000,10000,10000,10000)
AND (MSC_CODE ,CITY_ID,SERVICE_NODE_ID
,RUM_DATA_NUM ,DUP_SEQ_NUM ,SEIZ_CELL_NUM ,FLOW_DATA_INC 
,SUB_HOME_INT_PRI ,CON_OHM_NUM,SESS_SFC) 
< (150000,150000,150000,30,150000,6,150000
	,150000,150000,150000)
LIMIT 4000
ALLOW FILTERING ; 
